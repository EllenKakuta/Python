n1= input('Digite algo: ')
print('O tipo primitivo desse valor é',type(n1))
print('É alfanumérico? ',n1.isalnum())
print('É alfabetico? ',n1.isalpha())
print('Esta com as letras todas minúsculas? ',n1.islower())
print('Está com as letras todas maiúsculas? ',n1.isupper())
print('É numérico? ',n1.isnumeric())
print('Está capitalizada?', n1.istitle()) #Maiúscula e minúscula